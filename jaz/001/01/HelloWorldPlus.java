/*
Declare two strings, "Hello" and "World".

Make the console output "Hello, World!" using the strings.

Printing "Hello, World!" directly, without using the two Strings, is **not acceptable.**
*/

public class HelloWorldPlus{
   public static void main(String[] args) {
      String var1 = "Hello";
      String var2 = "World";

      System.out.println(var1 + ", " + var2 + "!");
   }
}
